public class RelationalOperators {
    public static void main(String[] args) {
        int num1 = 12;
        int num2 = 15;
        System.out.println(num1 > num2);
        System.out.println(num2 < num1);
        System.out.println(num1 >= num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
    }

}
